function numbersFrom1To100() {
    for (let i = 1; i <= 100; i++) {
        console.log(i);

    }

}
numbersFrom1To100();


function numbersFrom1To100() {
    for (let i = 1; i <= 100; i += 1) {
        console.log(i);

    }

}
numbersFrom1To100();


function numbersFrom1To100() {
    for (let i = 1; i <= 100; i = i + 1) {
        console.log(i);

    }

}
numbersFrom1To100();