// Concatenating
// ▪ Use the "+" or the "+=" operators:
let text = "Hello" + ", ";
// Expected output: "Hello, "
text += "JS!"; // "Hello, JS!"

// ▪ Use the concat() method:
let greet = "Hello, ";
let name = "John";
let result = greet.concat(name);
console.log(result); // Expected output: "Hello, John"
